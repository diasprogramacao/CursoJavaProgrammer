package entidade;

public class Pessoa {
	
	private String Nome;
	private String cpf;
	
	public void setNome(String nome) {
		this.Nome = nome;
	}

	public String getNome() {
		return this.Nome;
	}
	
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

	public String getCpf() {
		return this.cpf;
	}
	
	
}
